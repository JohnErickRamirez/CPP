// 2. Promedio de notas
#include <iostream>
using namespace std;

int main() {
    float nota, suma = 0.0;
    const int totalNotas = 5;
    for (int i = 1; i <= totalNotas; ++i) {
        cout << "Ingrese la nota " << i << ": ";
        cin >> nota;
        suma += nota;
    }
    cout << "El promedio es: " << (suma / totalNotas) << endl;
    return 0;
}
